def unicode_chars(charset)
  charsets[charset].map do |codepoint|
    character = [codepoint].pack("U*")
    "U+#{codepoint.to_s(16).upcase}: #{character}"
  end
end

unicode_chars(charset).each_slice(columns) do |row|
  puts row.join("   ")
end


